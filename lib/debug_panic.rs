macro_rules! debug_panic_if {//Different than debug_assert since it won't print an error like an
                             //assertion
    ($cond:expr $(,)?) => {
        #[cfg(debug_assertions)]
        if $cond {
            //debug_panic!();//TODO
            panic!();
        }
    };

    ($cond:expr, $($arg:tt)+) => {
        #[cfg(debug_assertions)]
        if $cond {
            //debug_panic!($($arg)+);//TODO
            panic!($($arg)+);
        }
    };
}
pub(crate) use debug_panic_if;

